def _format_ts(raw_ts: str) -> str:
    """Normalise an ISO8601 timestamp to 'YYYY-MM-DD HH:MM' or return as-is."""
    if not raw_ts:
        return ""
    try:
        # Handle numeric epoch seconds or milliseconds.
        if isinstance(raw_ts, (int, float)):
            ts_val = float(raw_ts)
            if ts_val > 1e10:
                ts_val /= 1000  # milliseconds → seconds
            dt = datetime.utcfromtimestamp(ts_val)
            return dt.strftime("%Y-%m-%d %H:%M")
        # ISO8601 string; strip trailing Z or +offset.
        clean = str(raw_ts)[:19].replace("T", " ")
        return clean
    except (ValueError, OSError):
        return str(raw_ts)[:19]


