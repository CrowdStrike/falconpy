def _parse_body_flexible(text: str) -> Optional[dict]:
    """Attempt to parse *text* as structured data, tolerating common non-JSON formats.

    Tries in order:
      1. Standard JSON (json.loads).
      2. Lua-table notation — ``["key"] = value`` — converted to JSON-like form.
      3. Regex heuristic: extract ``key = value`` or ``"key": value`` pairs.

    Returns a dict if any method succeeds, or ``None`` if the text is not
    parseable as a key-value structure.  Callers should treat the raw string as
    plain text when ``None`` is returned.
    """
    import re  # pylint: disable=import-outside-toplevel

    if not text:
        return None
    stripped = text.strip()

    # ── Pass 1: standard JSON ────────────────────────────────────────────────
    if stripped[:1] in ("{", "[", '"'):
        try:
            result = json.loads(stripped)
            if isinstance(result, dict):
                return result
            return None  # arrays / scalars handled by callers directly
        except (json.JSONDecodeError, ValueError):
            pass

    # Only attempt further parsing if the text looks like a table/struct.
    if stripped[:1] != "{":
        return None

    # ── Pass 2: Lua-table / annotated-JSON heuristic ─────────────────────────
    # Lua tables use  ["key"] = value  instead of  "key": value.
    # Also handles JS-style  key: value  (unquoted keys).
    # Steps:
    #   a) ["key"] = value  →  "key": value
    #   b) key = value  (bare identifier)  →  "key": value
    #   c) Remove trailing commas before } or ]
    #   d) Convert Lua true/false/nil → JSON true/false/null
    lua = stripped
    # a) ["key"] = expr  →  "key": expr
    lua = re.sub(r'\["([^"]+)"\]\s*=\s*', r'"\1": ', lua)
    # b) bare_key = expr  (only if looks like identifier, not already a string)
    lua = re.sub(r'(?<!["\w])([A-Za-z_][A-Za-z0-9_.]*)\s*=\s*(?!")', r'"\1": ', lua)
    # c) trailing commas before closing brackets
    lua = re.sub(r',\s*([}\]])', r'\1', lua)
    # d) Lua literals
    lua = re.sub(r'\btrue\b', 'true', lua)
    lua = re.sub(r'\bfalse\b', 'false', lua)
    lua = re.sub(r'\bnil\b', 'null', lua)

    try:
        result = json.loads(lua)
        if isinstance(result, dict):
            return result
    except (json.JSONDecodeError, ValueError):
        pass

    # ── Pass 3: pure regex extraction ────────────────────────────────────────
    # Extract all  "key": value  or  key = value  pairs from the raw text.
    pairs: dict = {}
    # Pattern A: quoted key, colon, value (JSON-like)
    for m in re.finditer(r'"([^"]+)"\s*[:=]\s*("(?:[^"\\]|\\.)*"|\d[\d.]*|true|false|null|\[[^\]]*\])', stripped):
        k = m.group(1)
        v_raw = m.group(2)
        try:
            pairs[k] = json.loads(v_raw)
        except (json.JSONDecodeError, ValueError):
            pairs[k] = v_raw.strip('"')
    # Pattern B: bare key = value (Lua-style, e.g. motd.enable = true)
    for m in re.finditer(r'([A-Za-z_][A-Za-z0-9_.]*)\s*=\s*("(?:[^"\\]|\\.)*"|\d[\d.]*|true|false|nil)', stripped):
        k = m.group(1)
        v_raw = m.group(2).replace("nil", "null")
        try:
            pairs[k] = json.loads(v_raw)
        except (json.JSONDecodeError, ValueError):
            pairs[k] = v_raw.strip('"')

    return pairs if pairs else None


def _extract_headline_from_body(body: str) -> str:
    """Return a short, readable headline extracted from a body string.

    When the TailoredIntelligence API returns content only in the *body* field
    and that field is JSON (or Lua-table / malformed JSON), using the raw string
    as a card headline produces an unreadable wall of braces.  This function
    extracts a meaningful single-line summary instead:

    - Plain text → returned as-is (truncated to 200 chars).
    - JSON object / Lua-table / key-value struct → looks for common title-like
      keys first (title, name, headline, subject, summary, message, text, type);
      falls back to the first non-empty string scalar value found; last resort
      is a compact key list.
    - JSON array → first element summarised; count appended if >1.
    - Other JSON scalars → converted to string.
    - Unparseable non-plain text → returned as-is (truncated).
    """
    if not body:
        return ""
    stripped = body.strip()
    if stripped[:1] not in ("{", "[", '"'):
        return body[:200]

    # Try standard JSON first; fall back to flexible parser for Lua/malformed.
    parsed = None
    try:
        parsed = json.loads(stripped)
    except (json.JSONDecodeError, ValueError):
        if stripped[:1] == "{":
            parsed = _parse_body_flexible(stripped)

    if parsed is None:
        return body[:200]

    if isinstance(parsed, dict):
        # Priority keys that are likely to contain a human-readable title.
        for key in ("title", "name", "headline", "subject", "summary", "message", "text", "type"):
            val = parsed.get(key)
            if isinstance(val, str) and val.strip():
                return val[:200]
        # Fall back to first non-empty string scalar in the dict.
        for val in parsed.values():
            if isinstance(val, str) and val.strip():
                return val[:200]
        # Non-string scalars (bool, int, float): format as "key: value" pairs so
        # the user sees the actual content, not just key names.
        scalar_pairs = [
            f"{k}: {str(v).lower() if isinstance(v, bool) else v}"
            for k, v in parsed.items()
            if isinstance(v, (bool, int, float))
        ]
        if scalar_pairs:
            summary = "  |  ".join(scalar_pairs[:4])
            if len(scalar_pairs) > 4:
                summary += f" (+{len(scalar_pairs) - 4} more)"
            return summary[:200]
        # Last resort: list the top-level keys so the card shows something useful.
        keys = list(parsed.keys())[:6]
        suffix = ", …" if len(parsed) > 6 else ""
        return "{" + ", ".join(keys) + suffix + "}"
    if isinstance(parsed, list):
        if not parsed:
            return "(empty)"
        first = parsed[0]
        summary = (
            _extract_headline_from_body(json.dumps(first))
            if isinstance(first, (dict, list)) else str(first)
        )
        if len(parsed) > 1:
            summary += f" (+{len(parsed) - 1} more)"
        return summary[:200]
    # Scalar JSON value.
    return str(parsed)[:200] if parsed is not None else ""


def _format_card_summary(raw: dict) -> str:
    """Build a compact key-value summary line for the EventCard.

    Accepts the raw API response dict for one event.  Extracts the most
    informative fields from top-level keys and the (possibly JSON-encoded)
    body so the user can read key details without drilling into the detail
    screen.

    Returns a single-line string like:
        "severity: high  |  tags: phishing, cred  |  rule: APT-123"
    """
    parts = []

    # Severity / confidence already shown in confidence row — skip duplication.
    # Try to extract structured fields from the raw body if it is JSON.
    body = raw.get("body", "")
    body_data: dict = {}
    if body and body.strip()[:1] in ("{", "["):
        try:
            parsed = json.loads(body)
            if isinstance(parsed, dict):
                body_data = parsed
        except (json.JSONDecodeError, ValueError):
            # Try flexible parser for Lua tables / malformed JSON.
            flexible = _parse_body_flexible(body)
            if isinstance(flexible, dict):
                body_data = flexible

    # severity
    severity = (
        body_data.get("severity")
        or body_data.get("severity_name")
        or raw.get("severity")
        or ""
    )
    if severity:
        parts.append(f"severity: {str(severity).lower()}")

    # tags / labels
    tags = (
        body_data.get("tags")
        or body_data.get("labels")
        or raw.get("tags")
        or []
    )
    if isinstance(tags, list) and tags:
        tag_str = ", ".join(str(t) for t in tags[:4])
        if len(tags) > 4:
            tag_str += f" +{len(tags) - 4}"
        parts.append(f"tags: {tag_str}")
    elif isinstance(tags, str) and tags:
        parts.append(f"tags: {tags}")

    # indicators / IOCs
    iocs = (
        body_data.get("indicators")
        or body_data.get("iocs")
        or raw.get("indicators")
        or raw.get("iocs")
        or []
    )
    if isinstance(iocs, list) and iocs:
        first_ioc = iocs[0]
        if isinstance(first_ioc, dict):
            first_ioc = first_ioc.get("value") or first_ioc.get("indicator") or str(first_ioc)
        ioc_str = str(first_ioc)
        if len(iocs) > 1:
            ioc_str += f" +{len(iocs) - 1}"
        parts.append(f"ioc: {ioc_str}")

    # matched rule (top-level matched_rules takes priority)
    matched = raw.get("matched_rules") or []
    rule_id = (matched[0].get("rule_id", "") if matched else "") or raw.get("rule_id") or ""
    if rule_id:
        parts.append(f"rule: {rule_id}")
    else:
        rule_name = (
            body_data.get("rule_name")
            or body_data.get("matched_rule")
            or body_data.get("rule")
            or ""
        )
        if rule_name:
            parts.append(f"rule: {str(rule_name)[:40]}")

    # actor / threat group
    actor = (
        body_data.get("actor")
        or body_data.get("threat_actor")
        or body_data.get("actor_name")
        or raw.get("actor")
        or ""
    )
    if actor:
        parts.append(f"actor: {str(actor)}")

    if not parts:
        # Fall back: list the top-level body keys as a breadcrumb
        if body_data:
            keys = list(body_data.keys())[:5]
            parts.append("fields: " + ", ".join(keys))
        else:
            return ""

    return "  |  ".join(parts)[:160]


def _format_description(raw_description: str) -> str:
    """Return a human-readable version of an event description.

    The TailoredIntelligence API sometimes returns the description field as a
    JSON-encoded string (or Lua-table / other malformed JSON) rather than plain
    prose.  This function attempts to decode it and render the contents as
    clearly labelled lines.

    Rendering rules:
    - Plain text (non-JSON) → returned unchanged.
    - JSON object / Lua-table / key-value struct → each top-level key rendered
      as "  Label: value".  Nested dicts are flattened one level; nested lists
      are joined with ", ".
    - JSON array (list) → each element rendered as a numbered bullet.
    - Any other JSON scalar → converted to string and returned.
    - Malformed / unparseable struct-like text → formatted as raw text block.
    """
    if not raw_description:
        return "(no description)"

    # Try to detect and parse JSON.  A description that doesn't start with
    # '{' or '[' is almost certainly plain text — skip the parse attempt.
    stripped = raw_description.strip()
    if stripped[:1] not in ("{", "[", '"'):
        return raw_description

    # Pass 1: standard JSON.
    parsed = None
    try:
        parsed = json.loads(stripped)
    except (json.JSONDecodeError, ValueError):
        # Pass 2: flexible parser for Lua tables / malformed JSON.
        if stripped[:1] == "{":
            parsed = _parse_body_flexible(stripped)

    if parsed is None:
        # Still not parseable — present as a formatted raw text block so at
        # least the content is legible rather than being raw JSON noise.
        return f"[raw]\n{raw_description}"

    if isinstance(parsed, dict):
        return _render_desc_dict(parsed)
    if isinstance(parsed, list):
        return _render_desc_list(parsed)
    # Scalar JSON value (string, number, bool, null).
    return str(parsed) if parsed is not None else "(no description)"


def _render_desc_dict(data: dict, indent: str = "  ") -> str:
    """Render a dict as labelled lines, one key per line.

    Keys are title-cased with underscores replaced by spaces for readability.
    Values that are themselves dicts are indented one further level.  List
    values are joined with '; ' to stay on a single line unless the list
    contains dicts (rendered as sub-bullets).
    """
    lines = []
    for key, value in data.items():
        label = key.replace("_", " ").replace("-", " ").title()
        if isinstance(value, dict):
            lines.append(f"{indent}{label}:")
            for sub_k, sub_v in value.items():
                sub_label = sub_k.replace("_", " ").replace("-", " ").title()
                lines.append(f"{indent}  {sub_label}: {sub_v}")
        elif isinstance(value, list):
            if not value:
                lines.append(f"{indent}{label}: (none)")
            elif all(isinstance(v, dict) for v in value):
                lines.append(f"{indent}{label}:")
                for item in value[:10]:
                    # Render each list-of-dicts item as a compact bullet.
                    parts = [f"{k}: {v}" for k, v in item.items()]
                    lines.append(f"{indent}  • {', '.join(parts)}")
                if len(value) > 10:
                    lines.append(f"{indent}  … and {len(value) - 10} more")
            else:
                joined = "; ".join(str(v) for v in value[:20])
                if len(value) > 20:
                    joined += f"; … (+{len(value) - 20})"
                lines.append(f"{indent}{label}: {joined}")
        elif value is None:
            lines.append(f"{indent}{label}: —")
        else:
            display = str(value)
            if len(display) > 200:
                display = display[:200] + "…"
            lines.append(f"{indent}{label}: {display}")
    return "\n".join(lines) if lines else "(no description)"


def _render_desc_list(items: list) -> str:
    """Render a JSON array as a numbered list of items."""
    if not items:
        return "(empty)"
    lines = []
    for i, item in enumerate(items[:30], start=1):
        if isinstance(item, dict):
            # Compact single-line representation of each dict item.
            parts = [f"{k}: {v}" for k, v in item.items()]
            lines.append(f"  {i}. {', '.join(parts)}")
        else:
            lines.append(f"  {i}. {item}")
    if len(items) > 30:
        lines.append(f"  … and {len(items) - 30} more")
    return "\n".join(lines)


