class AppState:
    """Mutable application state shared between the UI thread and worker threads."""

    def __init__(self):
        """Initialise state fields to safe defaults."""
        self.lock = threading.Lock()
        self.events: list[EventRecord] = []
        self.rules: list[RuleRecord] = []
        # Map event_id → EventRecord for O(1) detail look-up.
        self.events_by_id: dict[str, EventRecord] = {}
        # Map rule_id → RuleRecord for detail cross-reference.
        self.rules_by_id: dict[str, RuleRecord] = {}
        self.loading: bool = False
        self.error_message: str = ""
        # Active date-range selection: '24h', '7d', '30d', 'all'.
        self.date_range: str = "24h"
        # Pagination state for the events list.
