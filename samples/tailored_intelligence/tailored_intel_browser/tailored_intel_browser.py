    def __init__(self, client_id: str = "", client_secret: str = "", **kwargs): # nosec - Bandit FP
        """Initialise the app and its supporting components.

        *client_id* and *client_secret* may be provided from CLI args.  When
        non-empty they take precedence over environment variables during
        authentication.
        """
        super().__init__(**kwargs)
        # Store CLI-provided credentials for use in on_start().
        self._cli_client_id: str = client_id
        self._cli_client_secret: str = client_secret
        self.client = TailoredIntelClient()
        self.state = AppState()
        # References to key widgets populated in build().
        self._events_rv: Optional[EventsRecycleView] = None
        self._rules_rv: Optional[RulesRecycleView] = None
        self._loading_overlay: Optional[LoadingOverlay] = None
        self._error_overlay: Optional[ErrorOverlay] = None
        self._status_label = None
        self._event_count_label = None
        self._pagination_bar: Optional[PaginationBar] = None
        self._screen_manager: Optional[ScreenManager] = None
        self._detail_screen: Optional[DetailScreen] = None
        self._main_screen_widget = None

    # ------------------------------------------------------------------
    # Kivy lifecycle
    # ------------------------------------------------------------------

    def build(self):
        """Build and return the root widget tree."""
        Builder.load_string(KV)

        # ---- Root screen manager allows switching between list and detail ----
