        btn_rules.bind(on_press=lambda _: self._switch_tab("rules"))
        self._tab_btn_events = btn_events
        self._tab_btn_rules = btn_rules
        tab_bar.add_widget(btn_events)
        tab_bar.add_widget(btn_rules)
        root.add_widget(tab_bar)

        # Filter bar (only visible on events tab).
        self._filter_bar = FilterBar()
        self._event_count_label = self._filter_bar.ids.event_count_label
        root.add_widget(self._filter_bar)

