    def _make_tab_button(self, text: str, active: bool):
        """Create a styled tab Button widget."""
        from kivy.uix.button import Button
        from kivy.metrics import dp
        btn = Button(
            text=text,
            size_hint_x=None,
            width=dp(120),
            size_hint_y=None,
            height=dp(36),
            background_normal="",
            background_color=(0.2, 0.4, 0.8, 1) if active else (0.15, 0.15, 0.2, 1),
            color=(1, 1, 1, 1) if active else (0.7, 0.7, 0.7, 1),
            font_size="13sp",
        )
        return btn

    def on_start(self):
        """Called after build() — authenticate in background then load data."""
        self._set_status("connecting")

        def _auth_worker():
            ok = self.client.connect(
                client_id_override=self._cli_client_id,
                client_secret_override=self._cli_client_secret,
            )
            Clock.schedule_once(lambda dt: self._on_connect_done(ok), 0)

        threading.Thread(target=_auth_worker, daemon=True).start()

    def _on_connect_done(self, ok: bool):
        """Called on main thread after background auth completes."""
        if ok:
            self._set_status("connected")
            self._load_data()
        else:
            self._set_status("error")
            self._show_error(self.client.error)

    # ------------------------------------------------------------------
    # Tab switching
    # ------------------------------------------------------------------

    def _switch_tab(self, tab: str):
        """Switch the content area between 'events' and 'rules' views."""
        self._active_tab = tab
        self._content_area.clear_widgets()

        if tab == "events":
            self._content_area.add_widget(self._events_rv)
            self._filter_bar.opacity = 1
            self._filter_bar.disabled = False
            self._pagination_bar.opacity = 1
            self._pagination_bar.disabled = False
            self._tab_btn_events.background_color = (0.2, 0.4, 0.8, 1)
            self._tab_btn_events.color = (1, 1, 1, 1)
            self._tab_btn_rules.background_color = (0.15, 0.15, 0.2, 1)
            self._tab_btn_rules.color = (0.7, 0.7, 0.7, 1)
        else:
            self._content_area.add_widget(self._rules_rv)
            self._filter_bar.opacity = 0
            self._filter_bar.disabled = True
            self._pagination_bar.opacity = 0
            self._pagination_bar.disabled = True
            self._tab_btn_rules.background_color = (0.2, 0.4, 0.8, 1)
            self._tab_btn_rules.color = (1, 1, 1, 1)
            self._tab_btn_events.background_color = (0.15, 0.15, 0.2, 1)
            self._tab_btn_events.color = (0.7, 0.7, 0.7, 1)
            # Load rules if not yet loaded.
            with self.state.lock:
                rules_loaded = bool(self.state.rules)
            if not rules_loaded:
                self._load_rules()

    # ------------------------------------------------------------------
    # Date range
    # ------------------------------------------------------------------

    def on_date_range_pressed(self, range_key: str):
        """Handle a date range button press — rebuild the FQL filter and reload."""
        with self.state.lock:
            self.state.date_range = range_key
            self.state.current_page = 0
        # Update button highlights.
        range_btns = {
            "24h": self._filter_bar.ids.range_24h,
            "7d": self._filter_bar.ids.range_7d,
            "30d": self._filter_bar.ids.range_30d,
            "all": self._filter_bar.ids.range_all,
        }
        for key, btn in range_btns.items():
            if key == range_key:
                btn.background_color = (0.2, 0.5, 0.2, 1)
            else:
                btn.background_color = (0.15, 0.15, 0.2, 1)
        self._load_events()

    def _build_fql_filter(self) -> str:
        """Construct an FQL filter string for the active date range.

        Returns an empty string when the range is 'all' (no filter).
        The TailoredIntelligence API supports FQL filtering on created_date.
        """
        with self.state.lock:
            range_key = self.state.date_range

        delta_map = {"24h": timedelta(hours=24), "7d": timedelta(days=7), "30d": timedelta(days=30)}
        delta = delta_map.get(range_key)
        if delta is None:
            return ""  # 'all' — no filter

        since = (datetime.utcnow() - delta).strftime("%Y-%m-%dT%H:%M:%SZ")
        return f"created_date:>='{since}'"

    # ------------------------------------------------------------------
    # Data loading
    # ------------------------------------------------------------------

    def on_refresh_pressed(self):
        """Handle the top-bar Refresh button."""
        self._load_data()

    def _load_data(self):
        """Trigger background loads for both events and rules."""
        self._load_events()

    def _load_events(self):
        """Load events in a background thread."""
        with self.state.lock:
            if self.state.loading:
                return
            self.state.loading = True
        self._show_loading(True)

        fql = self._build_fql_filter()

        def _worker():
            try:
                events = self.client.fetch_events(fql_filter=fql)
                # Sort newest-first.
                events.sort(key=lambda e: e.timestamp_raw, reverse=True)
                with self.state.lock:
                    self.state.events = events
                    self.state.events_by_id = {e.id: e for e in events}
                    self.state.error_message = ""
            except Exception as exc:  # pylint: disable=broad-except
                with self.state.lock:
                    self.state.error_message = str(exc)
            finally:
                with self.state.lock:
                    self.state.loading = False
                # Schedule UI update on the main thread.
                Clock.schedule_once(self._on_events_loaded, 0)

        threading.Thread(target=_worker, daemon=True).start()

    def _load_rules(self):
        """Load rules in a background thread."""
        def _worker():
            try:
                rules = self.client.fetch_rules()
                with self.state.lock:
                    self.state.rules = rules
                    self.state.rules_by_id = {r.id: r for r in rules}
            except Exception as exc:  # pylint: disable=broad-except
                with self.state.lock:
                    self.state.error_message = str(exc)
            finally:
                Clock.schedule_once(self._on_rules_loaded, 0)

        threading.Thread(target=_worker, daemon=True).start()

    # ------------------------------------------------------------------
    # UI update callbacks (always run on the main/Kivy thread)
    # ------------------------------------------------------------------

    def _on_events_loaded(self, _dt):
        """Called on the main thread after event loading completes."""
        self._show_loading(False)
        with self.state.lock:
            events = list(self.state.events)
            err = self.state.error_message
            self.state.current_page = 0

        if err:
            # Show error but keep any existing data visible.
            self._set_status("error")
            return

        self._set_status("connected")
        self._rebuild_events_list(events)

    def _on_rules_loaded(self, _dt):
        """Called on the main thread after rule loading completes."""
        with self.state.lock:
            rules = list(self.state.rules)
        self._rebuild_rules_list(rules)

    def _rebuild_events_list(self, events: list):
        """Repopulate the events RecycleView from a list of EventRecord objects.

        Applies client-side pagination using state.current_page and state.page_size.
        """
        with self.state.lock:
            page = self.state.current_page
            page_size = self.state.page_size

        total = len(events)
        import math  # pylint: disable=import-outside-toplevel
        total_pages = max(1, math.ceil(total / page_size))
        # Clamp page in case events shrank after a filter change.
        page = max(0, min(page, total_pages - 1))
        with self.state.lock:
            self.state.current_page = page

        start = page * page_size
        end = start + page_size
        page_events = events[start:end]

        data = []
        for ev in page_events:
            data.append(
                {
                    "event_id": ev.id,
                    "event_type": ev.event_type,
                    "headline": ev.headline,
                    "card_summary": _format_card_summary(ev._raw),  # pylint: disable=protected-access
                    "confidence": _confidence_display(ev.confidence),
                    "timestamp": ev.timestamp,
                    "bg_color": ev.card_bg_color(),
                    "confidence_color": ev.confidence_color(),
                }
            )
        self._events_rv.data = data

        # Update event count label.
        if self._event_count_label:
            self._event_count_label.text = f"{total} event{'s' if total != 1 else ''}"

        # Update pagination UI.
        self._update_pagination_ui(page, total_pages, total)

    def _update_pagination_ui(self, page: int, total_pages: int, total: int):
        """Refresh the pagination bar labels and button states."""
        if self._pagination_bar is None:
            return
        ids = self._pagination_bar.ids
        # Page info label.
        if total == 0:
            ids.page_info_label.text = "No events"
        else:
            ids.page_info_label.text = f"Page {page + 1} / {total_pages}"
        # Enable/disable Prev and Next.
        ids.prev_btn.disabled = page == 0
        ids.prev_btn.background_color = (
            (0.2, 0.4, 0.8, 1) if page > 0 else (0.1, 0.1, 0.15, 1)
        )
        ids.next_btn.disabled = page >= total_pages - 1
        ids.next_btn.background_color = (
            (0.2, 0.4, 0.8, 1) if page < total_pages - 1 else (0.1, 0.1, 0.15, 1)
        )
        # Highlight the active page size button.
        with self.state.lock:
            active_ps = self.state.page_size
        active_color = (0.2, 0.5, 0.2, 1)
        inactive_color = (0.15, 0.15, 0.2, 1)
        ids.ps_25.background_color = active_color if active_ps == 25 else inactive_color
        ids.ps_50.background_color = active_color if active_ps == 50 else inactive_color
        ids.ps_100.background_color = active_color if active_ps == 100 else inactive_color

    # ------------------------------------------------------------------
    # Pagination actions
    # ------------------------------------------------------------------

    def on_prev_page(self):
        """Navigate to the previous event page."""
        with self.state.lock:
            if self.state.current_page <= 0:
                return
            self.state.current_page -= 1
            events = list(self.state.events)
        self._rebuild_events_list(events)

    def on_next_page(self):
        """Navigate to the next event page."""
        with self.state.lock:
            events = list(self.state.events)
            page_size = self.state.page_size
        import math  # pylint: disable=import-outside-toplevel
        total_pages = max(1, math.ceil(len(events) / page_size))
        with self.state.lock:
            if self.state.current_page >= total_pages - 1:
                return
            self.state.current_page += 1
        self._rebuild_events_list(events)

    def on_page_size_change(self, size: int):
        """Change the number of events shown per page and reset to page 0."""
        with self.state.lock:
            self.state.page_size = size
            self.state.current_page = 0
            events = list(self.state.events)
        self._rebuild_events_list(events)

    def _rebuild_rules_list(self, rules: list):
        """Repopulate the rules RecycleView from a list of RuleRecord objects."""
        data = []
        for rule in rules:
            data.append(
                {
                    "rule_name": rule.name,
                    "rule_type": rule.rule_type,
                    "rule_status": rule.status_text(),
                    "rule_id": f"ID: {rule.id[:32]}…" if len(rule.id) > 32 else f"ID: {rule.id}",
                    "rule_status_color": rule.status_color(),
                }
            )
        self._rules_rv.data = data

    # ------------------------------------------------------------------
    # Detail screen
    # ------------------------------------------------------------------

    def open_event_detail(self, event_id: str):
        """Navigate to the detail screen for the given event ID."""
        with self.state.lock:
            ev = self.state.events_by_id.get(event_id)
            rules_by_id = dict(self.state.rules_by_id)

        if ev is None:
            return

        # Find the source rule, if any.
        rule_text = "(no source rule linked)"
        if ev.rule_id:
            rule = rules_by_id.get(ev.rule_id)
            if rule:
                rule_text = f"{rule.name} (type: {rule.rule_type}, {rule.status_text()})"
            else:
                rule_text = f"Rule ID: {ev.rule_id}"

        # Populate the detail screen properties.
        ds = self._detail_screen
        ds.event_title = ev.headline or "(no title)"
        ds.event_type_text = f"Type: {ev.event_type}"
        ds.event_ts_text = f"Timestamp: {ev.timestamp or '(unknown)'}"
        ds.event_confidence_text = f"Confidence: {_confidence_display(ev.confidence)}"
        ds.event_desc_text = _format_description(ev.description)
        ds.event_indicators_text = _format_indicators(ev.indicators)
        ds.event_rule_text = rule_text
        ds.event_raw_text = _format_raw(ev._raw)

        self._screen_manager.current = "detail"

    def go_back(self):
        """Navigate back to the main list screen."""
        self._screen_manager.current = "main"

    # ------------------------------------------------------------------
    # Status / overlay helpers
    # ------------------------------------------------------------------

    def _set_status(self, status: str):
        """Update the connection status label in the top bar."""
        labels = {
            "connecting": ("● Connecting…", (0.9, 0.8, 0.2, 1)),
            "connected": ("● Connected", (0.3, 0.85, 0.4, 1)),
            "error": ("● Auth Error", (0.85, 0.3, 0.3, 1)),
            "disconnected": ("● Disconnected", (0.6, 0.6, 0.6, 1)),
        }
        text, color = labels.get(status, labels["disconnected"])
        if self._status_label:
            self._status_label.text = text
            self._status_label.color = color

    def _show_loading(self, visible: bool):
        """Show or hide the loading overlay."""
        if self._loading_overlay is None:
            return
        if visible:
            self._loading_overlay.opacity = 1
            self._loading_overlay.disabled = False
        else:
            self._loading_overlay.opacity = 0
            self._loading_overlay.disabled = True

    def _show_error(self, message: str):
        """Display the full-screen authentication error overlay."""
        if self._error_overlay is None:
            return
        self._error_overlay.error_message = message
        self._error_overlay.opacity = 1
        self._error_overlay.disabled = False


