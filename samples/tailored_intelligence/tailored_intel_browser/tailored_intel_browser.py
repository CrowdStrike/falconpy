class TailoredIntelClient:
    """Thin wrapper around the FalconPy TailoredIntelligence service class."""

    # Maximum IDs per batch GET request.
    _BATCH_SIZE = 100

    def __init__(self):
        """Construct without authenticating — call connect() first."""
        self._sdk: Optional[TailoredIntelligence] = None
        self.status: str = "disconnected"
        self.error: str = ""

    def connect(
        self,
        client_id_override: str = "",
        client_secret_override: str = "", # nosec - Bandit FP
    ) -> bool:
        """Authenticate using provided credentials or env vars.

        Credential resolution order (first non-empty value wins):
          1. *client_id_override* / *client_secret_override* (CLI args)
          2. FALCON_CLIENT_ID / FALCON_CLIENT_SECRET environment variables

        Returns True on success, False on failure.  Sets self.error on failure.
        """
        client_id = client_id_override or os.environ.get("FALCON_CLIENT_ID", "")
        client_secret = client_secret_override or os.environ.get("FALCON_CLIENT_SECRET", "")
        if not client_id or not client_secret:
            self.error = (
                "Credentials not provided. Supply -k/-s arguments or set "
                "FALCON_CLIENT_ID / FALCON_CLIENT_SECRET environment variables."
            )
            self.status = "error"
            return False
        try:
            sdk = TailoredIntelligence(
                client_id=client_id, client_secret=client_secret
            )
            if not sdk.token_valid:
                self.error = (
                    f"Authentication failed: {sdk.token_fail_reason}"
                )
                self.status = "error"
                return False
            self._sdk = sdk
            self.status = "connected"
            self.error = ""
            return True
        except Exception as exc:  # pylint: disable=broad-except
            self.error = str(exc)
            self.status = "error"
            return False

    def fetch_events(
        self,
        fql_filter: str = "",
        limit: int = 200,
    ) -> list:
        """Return a list of EventRecord objects matching the optional FQL filter.

        Paginates via offset until *limit* events are collected or results are
        exhausted.
        """
        if self._sdk is None:
            return []

        # Collect event IDs via query_events.
        all_ids: list[str] = []
        offset = 0
        page_size = min(100, limit)

        while len(all_ids) < limit:
            kwargs: dict = {"limit": page_size, "offset": offset}
            if fql_filter:
                kwargs["filter"] = fql_filter

            resp = self._sdk.query_events(**kwargs)
            if resp.get("status_code", 0) not in (200, 201):
                break

            body = resp.get("body", {})
            ids_page: list = body.get("resources", [])
            if not ids_page:
                break

            all_ids.extend(ids_page)
            total = body.get("meta", {}).get("pagination", {}).get("total", 0)
            offset += len(ids_page)

            if offset >= total or len(all_ids) >= limit:
                break

        if not all_ids:
            return []

        # Fetch event entities in batches.
        records: list[EventRecord] = []
        for i in range(0, len(all_ids), self._BATCH_SIZE):
            batch = all_ids[i : i + self._BATCH_SIZE]
            resp = self._sdk.get_event_entities(ids=batch)
            if resp.get("status_code", 0) not in (200, 201):
                continue
            for raw in resp.get("body", {}).get("resources", []):
                records.append(EventRecord(raw))

        return records

    def fetch_rules(self, limit: int = 200) -> list:
        """Return a list of RuleRecord objects for active tailored intelligence rules.

        Paginates via offset until *limit* rules are collected or results are
        exhausted.
        """
        if self._sdk is None:
            return []

        all_ids: list[str] = []
        offset = 0
        page_size = min(100, limit)

        while len(all_ids) < limit:
            resp = self._sdk.query_rules(limit=page_size, offset=offset)
            if resp.get("status_code", 0) not in (200, 201):
                break

            body = resp.get("body", {})
            ids_page: list = body.get("resources", [])
            if not ids_page:
                break

            all_ids.extend(ids_page)
            total = body.get("meta", {}).get("pagination", {}).get("total", 0)
            offset += len(ids_page)

            if offset >= total or len(all_ids) >= limit:
                break

        if not all_ids:
            return []

        records: list[RuleRecord] = []
        for i in range(0, len(all_ids), self._BATCH_SIZE):
            batch = all_ids[i : i + self._BATCH_SIZE]
            resp = self._sdk.get_rule_entities(ids=batch)
            if resp.get("status_code", 0) not in (200, 201):
                continue
            for raw in resp.get("body", {}).get("resources", []):
                records.append(RuleRecord(raw))

        return records


